import Head from 'next/head';
import React from 'react';
import useSWR, { SWRConfig } from 'swr';
import { NextPageWithLayout } from '../../types/global';
import { apiFetcher } from '../../services/apiFetcher';
import { search } from '../../services/search';

const getServerSideProps = async () => {
  const entities = await search();

  return {
    props: {
      fallback: {
        search: entities,
      },
    },
  };
};

const Library: NextPageWithLayout = ({ fallback }) => {
  const { data } = useSWR('search', apiFetcher);
  return (
    <>
      <Head>
        <title>Uwazi test route</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1 className="text-3xl font-bold underline">This page should render entities JSON:</h1>
      <SWRConfig value={{ fallback }}>
        <pre>{JSON.stringify(data, null, 2)}</pre>
      </SWRConfig>
    </>
  );
};

export { getServerSideProps };
export default Library;
